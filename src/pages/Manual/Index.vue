    <h1  id="h.3cybpawmc5db"><span >PR&Eacute;STAMOS</span></h1>
    <p ><span >&nbsp;</span></p>
    <p ><span >Los jugadores pueden pedir prestado dinero a la Banca en cualquier Campa&ntilde;a e
            ingresarlo en sus Arcas del Tesoro al principio de la siguiente, con las siguientes limitaciones:</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >El
            n&uacute;mero m&aacute;ximo de ducados que se puede pedir no puede exceder 25, sin incluir intereses.</span>
    </p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >No se
            pude realizar un nuevo pr&eacute;stamo si ya se tiene uno contratado y no se ha acabado de saldar la
            deuda.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >Los
            pr&eacute;stamos se hacen a uno o dos a&ntilde;os. Los pr&eacute;stamos deben ser pagados (con intereses) al
            final de la Campa&ntilde;a en la que vencen (o antes, aunque no se rebajan los intereses por ello).</span>
    </p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >Los
            intereses que cobra el Banco son del 20% si el dinero se presta a un a&ntilde;o y del 50% si se prestan a
            dos a&ntilde;os. Las fracciones se redondean hacia arriba.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >En el caso de que un jugador no puede afrontar el pago de una deuda al Banco, el
            efecto ser&aacute; el mismo que si ese jugador hubiera sido asesinado. La deuda queda cancelada a partir de
            entonces, pero no podr&aacute; pedir nunca m&aacute;s dinero al Banco.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Los jugadores tambi&eacute;n pueden prestarse ducados entre ellos sin l&iacute;mite.
            Igual que los pr&eacute;stamos con la Banca, la transferencia se har&aacute; efectiva al principio de la
            siguiente campa&ntilde;a. Para que no haya posibilidad de trampas o malentendidos, todas estas interacciones
            pasan por el DJ. </span></p>
