    <h1  id="h.r89kvbgaqm8j"><span >FASE DE EJECUCI&Oacute;N. RESOLUCI&Oacute;N DE
            CONFLICTOS</span></h1>
    <p ><span >&nbsp;</span></p>
    <p ><span >Una vez con todas las &oacute;rdenes de los jugadores en su poder, el DJ resuelve
            todas sus &oacute;rdenes. Esta secci&oacute;n indica qu&eacute; criterios se siguen para resolver las
            acciones que las unidades militares.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >En primer lugar, TODAS LAS ACCIONES DE TODOS LOS JUGADORES SE RESUELVEN
            SIMULTANEAMENTE.</span></p>
    <p ><span >&nbsp;</span></p>
    <h2  id="h.1v61y0g0v8v"><span >Conflictos</span><span >&nbsp;</span></h2>
    <p ><span >&nbsp;</span></p>
    <p ><span >Los conflictos ocurren cuando cualquier unidad militar tiene una orden que
            causar&iacute;a que m&aacute;s de una unidad ocupe el mismo lugar (regi&oacute;n o mar) a la vez. Los
            conflictos pueden ocurrir entre bandos que tienen la misma fuerza o bandos que tienen fuerzas distintas.
            Para que un conflicto ocurra, uno o m&aacute;s bandos deben estar intentando avanzar, fusionar o convertir
            en el lugar disputado. Los conflictos pueden ser simples o bastante complejos, implicando varias unidades.
            Las reglas dadas en esta secci&oacute;n se aplicar&aacute;n a todos los conflictos.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Fuerzas</span><span >: la fuerza de una unidad viene determinada por
            su nivel, indicado con un n&uacute;mero en el mapa. La fuerza de un bando es la suma de los niveles de todas
            las unidades que componen ese bando.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Bandos</span><span >: un bando puede consistir en una sola unidad
            militar o puede consistir en una sola unidad militar m&aacute;s todas las unidades militares que la apoyan.
            Hay que observar que, por ejemplo, tres unidades separadas con &oacute;rdenes de avanzar al mismo sitio
            constituyen tres bandos, cada una con su fuerza. S&iacute; s&oacute;lo una de ellas est&aacute; avanzando,
            mientras que las otras dos unidades tienen &oacute;rdenes de apoyar al lugar donde se avanza, ser&iacute;a
            un solo bando con una fuerza combinada de las tres.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Un bando no tiene por qu&eacute; estar formado por unidades de un mismo jugador.
            Recuerda que pueden apoyarse unidades de otros jugadores.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Distinguimos entre varios tipos de conflictos:</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>1.</span><span >&nbsp;
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span><span >Conflictos entre bandos de igual
            fuerza</span><span >. Si las &oacute;rdenes de dos o m&aacute;s bandos de igual fuerza entran en
            conflicto, ning&uacute;n movimiento puede ser hecho. A continuaci&oacute;n se explican situaciones
            espec&iacute;ficas de conflicto entre bandos de fuerzas iguales:</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >o</span><span >&nbsp; &nbsp;</span><span >Si dos o
            m&aacute;s bandos de igual fuerza tienen &oacute;rdenes de avanzar al mismo sitio, ninguno de ellos puede
            entrar en el sitio. Esta es la situaci&oacute;n de &ldquo;empate&rdquo;, y ninguna otra unidad puede ser
            retirada al sitio donde no se pudo avanzar (ver &ldquo;Retiradas&rdquo; m&aacute;s abajo).</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >o</span><span >&nbsp; &nbsp;</span><span >Si dos bandos de
            igual fuerza tienen &oacute;rdenes de avanzar, cada uno al lugar que el otro ocupa, ninguno de ellos
            avanza.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >o</span><span >&nbsp; &nbsp;</span><span >Si un bando
            avanza con &eacute;xito a otro sitio diferente mientras otro bando entra en su lugar original, no ocurren
            conflictos.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >o</span><span >&nbsp; &nbsp;</span><span >Un bando con
            &oacute;rdenes de avanzar a un sitio ocupado por otra unidad de misma fuerza (con &oacute;rdenes de
            mantener, transportar, convertir) no puede avanzar, y la unidad ocupante puede llevar a cabo sus
            &oacute;rdenes.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >o</span><span >&nbsp; &nbsp;</span><span >Un bando con
            &oacute;rdenes de avanzar a un sitio ya ocupado por otra unidad de la misma fuerza con &oacute;rdenes de
            apoyar no puede avanzar, pero la unidad ocupante no puede llevar a cabo sus &oacute;rdenes, a no ser que el
            avance venga desde el sitio al que est&aacute; apoyando directamente.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Por ejemplo, una unidad que apoya el ataque de otra unidad no ver&aacute;
            interrumpido su apoyo si es atacada desde la provincia destino del ataque est&aacute; apoyando. Sin embargo,
            su apoyo se ver&aacute; interrumpido si es atacada desde cualquier otro sitio.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >o</span><span >&nbsp; &nbsp;</span><span >Una unidad con
            &oacute;rdenes de avanzar o fusionar y que no puede hacerlo debido a un conflicto de fuerzas iguales
            impedir&aacute; que otra unidad de igual fuerza avance al lugar que &eacute;sta ocupa.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >SUGERENCIA Y ESTRATEGIA: a la hora de planear acciones que se prevean
            conflictivas, hay que tener muy en cuenta todas las variables, tanto si eres el jugador que provoca el
            conflicto como si eres el que lo sufre y lo tiene que rechazar. No es raro ver c&oacute;mo s&oacute;lidas
            redes de apoyo son rotas por avances furtivos desde zonas insospechadas que rompen los apoyos, generalmente
            por la retaguardia.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>2.</span><span >&nbsp;
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span><span >Conflictos entre bandos de fuerza
            desigual</span><span >. Si las &oacute;rdenes de dos bandos de fuerzas desiguales entran en
            conflicto, el lado con m&aacute;s fuerza siempre gana. El jugador que puede aportar m&aacute;s fuerzas
            (incluyendo los apoyos con los que cuenta) ganar&aacute;, forzando al bando m&aacute;s d&eacute;bil a
            permanecer en su lugar de origen o retirarse, seg&uacute;n sea la situaci&oacute;n. Abajo, se explican
            situaciones de conflicto espec&iacute;ficas entre bandos de fuerzas desiguales:</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >o</span><span >&nbsp; &nbsp;</span><span >Si un bando
            superior tiene &oacute;rdenes de avanzar a un lugar al mismo tiempo que un bando inferior tiene
            &oacute;rdenes</span></p>
    <p ><span >+ de avanzar al mismo sitio, el bando superior avanzar&aacute;, mientras que
            el/los bando/s inferior/es permanecer&aacute;n all&iacute; donde estuvieran.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >o</span><span >&nbsp; &nbsp;</span><span >Si un bando
            superior tiene &oacute;rdenes de avanzar a un sitio ocupado por un bando de fuerza inferior, el bando
            superior avanza y el bando inferior debe retirarse.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >o</span><span >&nbsp; &nbsp;</span><span >Si dos bandos
            tienen &oacute;rdenes de avanzar, cada uno al sitio que ocupa el otro, el bando superior avanzar&aacute; y
            el bando inferior debe retirarse.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >o</span><span >&nbsp; &nbsp;</span><span >Si un bando de
            fuerza superior tiene &oacute;rdenes de mantenerse en el sitio, y una fuerza inferior intenta avanzar, el
            avance no puede realizarse.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >o</span><span >&nbsp; &nbsp;</span><span >Una unidad con
            &oacute;rdenes de fusionar que no pueda hacerlo porque la unidad de destino ha debido retirarse no
            podr&aacute; cumplir sus &oacute;rdenes y permanecer&aacute; en su regi&oacute;n de origen.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >o</span><span >&nbsp; &nbsp;</span><span >Si una unidad
            resultante de una divisi&oacute;n intenta avanzar a una regi&oacute;n que genere un conflicto con otra
            fuerza superior, dicha divisi&oacute;n no se llevar&aacute; a cabo.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>3.</span><span >&nbsp;
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span><span >Conflictos entre tus propias
            unidades. Una unidad s&oacute;lo puede entrar en una regi&oacute;n en la que ya exista otra unidad del mismo
            tipo y del mismo jugador si es bajo las &oacute;rdenes de fusionarse con ella. En cualquier otro caso se
            considera una acci&oacute;n err&oacute;nea (es decir, como si se le hubiera dado la orden de mantener).
        </span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >En ning&uacute;n momento puede haber 2 unidades en una misma regi&oacute;n, sean
            del tipo que sean, y bajo ning&uacute;n concepto se permite el conflicto con tus propias unidades.</span>
    </p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Tampoco puede reclutarse una unidad en una regi&oacute;n en la que ya haya otra
            unidad para fusionarla con la existente. Para fusionar 2 unidades, estas deben estar inicialmente en 2
            regiones anexas.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>4.</span><span >&nbsp;
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span><span >Conflictos complejos entre bandos de
            muchos jugadores. Puede darse el caso de que el conflicto se produzca entre m&aacute;s de dos jugadores.
            Imagina la situaci&oacute;n, improbable pero te&oacute;ricamente posible: cinco bandos, cada uno de un
            jugador, intentando avanzar a la misma regi&oacute;n.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >En estos casos, si hay una fuerza superior ganar&aacute; siempre, y el resto de
            bandos deben retirarse o permanecer en el lugar de origen, seg&uacute;n sea la situaci&oacute;n. Si no hay
            una &uacute;nica fuerza que sea superior a las dem&aacute;s se toma como si todos los bandos tuvieran la
            misma fuerza, aunque esto no sea cierto (en el ejemplo anterior, supongamos que tenemos dos bandos con
            fuerza uno, uno con fuerza dos y dos con fuerza tres; como no hay una fuerza superior, lo tratamos como si
            todos tuvieran la misma fuerza).</span></p>
    <p ><span >&nbsp;</span></p>
    <h3  id="h.wk6v407fx09i"><span >Retiradas</span></h3>
    <p ><span >&nbsp;</span></p>
    <p ><span >Una unidad militar est&aacute; forzada a retirarse cuando una fuerza oponente
            superior tiene orden de avanzar hacia su regi&oacute;n o mar. </span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Una unidad militar s&oacute;lo puede retirarse a un lugar no ocupado al que
            podr&iacute;a haberse movido normalmente, sin incluir transporte, es decir, a una regi&oacute;n o mar
            adyacente. Incluso aunque en la provincia a la que se quiera retirar haya una unidad de nivel o fuerza
            inferior a la de la unidad que se retira.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Ni las unidades de flota pueden retirarse a una regi&oacute;n no costera, ni las
            unidades de ej&eacute;rcito pueden retirarse a un mar.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Las unidades pueden retirarse a regiones anexas vac&iacute;as salvo en las siguientes
            situaciones:</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >No pueden
            retirarse a regiones o mares en las que haya habido una situaci&oacute;n de empate.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >No pueden
            retirarse a la regi&oacute;n o mar por donde entr&oacute; la fuerza invasora.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >Si una
            unidad no tiene ning&uacute;n lugar al que retirarse y debe hacerlo, la unidad es retirada del juego.</span>
    </p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >Una
            retirada cancela inmediatamente las ordenes de avanzar, mantener, fusionar, apoyar o transportar en la
            unidad que la sufre. No cancela &oacute;rdenes de convertir. Una unidad de flota o ej&eacute;rcito que tenga
            orden de convertirse lo har&aacute;, pero tendr&aacute; que retirarse inmediatamente ante las fuerzas
            enemigas.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >El lugar al que una unidad se retira lo decide el jugador de antemano (respetando las
            limitaciones anteriormente indicadas) en el mismo e-mail en el que indican las &oacute;rdenes para sus
            unidades. Para cada unidad se deber&aacute; indicar una lista de lugares a los que esa unidad se
            retirar&aacute; en caso de que se vea obligada a ello. Se pueden indicar tantos lugares como se deseen:
            ninguno (si est&aacute;s seguro de que esa unidad no va a ser forzada a retirarse) o varios (indicando orden
            de preferencia).</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >De producirse la retirada, la decisi&oacute;n se toma en funci&oacute;n de esa lista.
            Si no se ha proporcionado una lista o todos los lugares de la misma son inaplicables (pe, porque en el
            transcurso del turno los han ocupado otras unidades) la unidad se retirar&aacute; al azar (de forma
            equiprobable entre todas las regiones/mares adyacentes legales).</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Se puede dar el caso en que una unidad, al retirarse, deba entrar en una provincia
            que est&eacute; vac&iacute;a pero no sea de su misma facci&oacute;n. No hay limitaciones al respecto y la
            provincia en cuesti&oacute;n pasar&aacute; a manos del jugador que ha tenido que retirarse.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Obviamente, cuando los jugadores toman estas decisiones, no saben con certeza
            c&oacute;mo ser&aacute; el desarrollo del turno y si los lugares indicados para la retirada seguir&aacute;n
            siendo v&aacute;lidos (de ah&iacute; que se puedan indicar varias alternativas posibles). De esa forma, se
            a&ntilde;ade un nuevo factor de habilidad al juego. El buen jugador, sabr&aacute; prever las posibles
            retiradas y actuar en consecuencia, mientras que el mal jugador tendr&aacute; que confiar en el azar.</span>
    </p>
