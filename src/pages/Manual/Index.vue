    <h2  id="h.qr0a28yyha86"><span >Unidades de ej&eacute;rcito</span></h2>
    <p ><span >&nbsp;</span></p>
    <p ><span >Cada nivel de fuerza de una unidad de ej&eacute;rcito cuesta 3 ducados, tanto para
            crearlas como para mantenerlas. Pueden ser reclutadas en cualquier regi&oacute;n que forme parte del
            pa&iacute;s natal del jugador. </span></p>
    <p ><span >&nbsp;</span></p>
