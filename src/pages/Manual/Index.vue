    <h2  id="h.8ljg82s1sb6p"><span ></span></h2>
    <h2  id="h.2olhl01nim0c"><span >Unidades de flota</span></h2>
    <p ><span >&nbsp;</span></p>
    <p ><span >Cada nivel de fuerza de una unidad de flota tiene un coste m&iacute;nimo de 3 ducados
            y pueden ser reclutadas en cualquier regi&oacute;n con puerto que forme parte del pa&iacute;s natal de
            jugador.</span></p>
    <p ><span >&nbsp;</span></p>
