    <h1  id="h.2v35n350uron"><span >FASE DE ESCRIBIR &Oacute;RDENES</span></h1>
    <p ><span >&nbsp;</span></p>
    <p ><span >Esta fase la realiza el jugador, comunicando por escrito al DJ las acciones que
            quiere que hagan todas sus tropas. Las &oacute;rdenes que debe dar son de dos tipos:</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >&Oacute;rdenes de Gastos Extra</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >El jugador puede realizar desde ninguna hasta un m&aacute;ximo de cuatro acciones
            de &ldquo;gastos extra&rdquo;. Los gastos extras son de naturaleza muy diversa e implican siempre el gasto
            de una determinada cantidad de oro, a diferencia de las unidades militares, que no requieren pago de
            ning&uacute;n tipo. Las &oacute;rdenes de gastos extras se resuelven en primer lugar, antes que las
            &oacute;rdenes para las unidades.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >&Oacute;rdenes para las unidades militares</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Estas &oacute;rdenes reflejan qu&eacute; har&aacute; cada uno de los
            ej&eacute;rcitos, flotas y corsarios del jugador, y deben indicar d&oacute;nde se encuentra y el tipo de
            acci&oacute;n que se quiere realizar con esa unidad.</span></p>
    <p ><span >&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></p>
    <p ><span >Hay que poner especial cuidado en la notaci&oacute;n, ya que cualquier orden que no
            est&eacute; escrita tal y como se indica en las secciones &ldquo;&Oacute;rdenes para unidades
            militares&rdquo;, o &ldquo;Gastos extras&rdquo;, ser&aacute; tomada como error. Si se produce un error al
            escribir la orden de una unidad militar, esa unidad no har&aacute; nada ese turno, tal y como si se le
            hubiera dado una orden de &ldquo;Mantener. Si se produce un error al escribir una orden de gasto extra, no
            se realizar&aacute; ninguna orden.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >&nbsp;</span></p>
