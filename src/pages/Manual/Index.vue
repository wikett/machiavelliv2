    <h4  id="h.449ctaqylrq3"><span >Sobornos</span></h4>
    <p ><span >&nbsp;</span></p>
    <p ><span >Hay de varios tipos y cada uno de ellos tiene un coste espec&iacute;fico.</span>
    </p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Disolver unidad aut&oacute;noma</span><span >. Con esta orden,
            intentas retirar del juego una unidad aut&oacute;noma. Su coste m&iacute;nimo es 6d.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Comprar unidad aut&oacute;noma</span><span >. Con esta orden,
            intentas convertir una unidad aut&oacute;noma en una de tus propias unidades. Su coste m&iacute;nimo es
            9d.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Convertir unidad no aut&oacute;noma en aut&oacute;noma</span><span >.
            Con esta orden, intentas convertir una unidad de un jugador en una unidad aut&oacute;noma. Su coste
            m&iacute;nimo es 9d.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Disolver unidad de ej&eacute;rcito o flota</span><span >. Con esta
            orden intentas retirar del juego una unidad de ej&eacute;rcito o flota de otro jugador. Su coste
            m&iacute;nimo es de 12d.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Comprar unidad de ej&eacute;rcito o flota</span><span >. Con esta
            orden intentas convertir una unidad de ej&eacute;rcito o flota de otro jugador en una de tus propias
            unidades. Su coste m&iacute;nimo es de 24d.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Los sobornos no tienen &eacute;xito autom&aacute;tico. Las posibilidades de
            &eacute;xito dependen:</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >Del
            dinero a emplear</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >De si la
            unidad objeto del soborno est&aacute; protegida con contra-sobornos de otro jugador y de qu&eacute; cantidad
            haya invertido &eacute;ste.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >La regla
            es que un soborno tiene &eacute;xito si la cantidad de dinero empleado, menos la cantidad de dinero de los
            contra-sobornos es igual o superior al coste m&iacute;nimo del soborno en cuesti&oacute;n.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >SUGERENCIA Y ESTRATEGIA: es conveniente a la hora de hacer un soborno, poner una
            cantidad de dinero a emplear ligeramente superior del m&iacute;nimo. Esto se hace para evitar los
            contra-sobornos, ya que es terriblemente frustrante que, habiendo realizado un soborno de 24d te lo eviten
            con un simple contra-soborno de 3d sobre esa unidad, perdiendo t&uacute; una considerable cantidad de
            dinero.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Cuando haya m&aacute;s de un soborno sobre una misma unidad en una misma
            campa&ntilde;a, s&oacute;lo se toma el m&aacute;s alto, los dem&aacute;s quedan desperdiciados y el dinero
            se pierde. Los contra-sobornos se aplican al soborno que permanezca. Cuando haya un empate entre los dos
            superiores, todos los sobornos quedan desperdiciados.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >S&oacute;lo se puede sobornar a unidades en regiones directamente adyacentes a una
            de las unidades militares del jugador que realiza el soborno.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Cuando se ha realizado un soborno de compra, pueden escribirse las &oacute;rdenes
            para dicha unidad previendo su adquisici&oacute;n.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >&nbsp;</span></p>
    <h4  id="h.jejci2z109qf"><span >Rebeliones</span></h4>
    <p ><span >&nbsp;</span></p>
    <p ><span >Estas acciones se utilizan contra regiones controladas por el enemigo, aunque
            tenga unidades militares todav&iacute;a en ellas. A efectos del juego, esta acci&oacute;n coloca un marcador
            de rebeli&oacute;n en la regi&oacute;n indicada. Los contra-sobornos no pueden ser usados para evitar
            revoluciones.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Los costes para incitar una rebeli&oacute;n son:</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span
            >Regi&oacute;n que NO forma parte del pa&iacute;s natal de un jugador &ndash; 9 ducados.</span>
    </p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span
            >Regi&oacute;n que forma parte del pa&iacute;s natal de un jugador &ndash; 15 ducados</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Consecuencias de la rebeli&oacute;n</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Cuando una regi&oacute;n se rebela, lo hace contra un solo color o jugador, el
            que la controlaba en el momento en el que se produjo dicha rebeli&oacute;n. Las regiones con un marcador de
            rebeli&oacute;n est&aacute;n sujetas a las siguientes reglas:</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >Los
            marcadores de rebeli&oacute;n no pueden ser retirados excepto por los siguientes medios: pacificando la
            rebeli&oacute;n, someti&eacute;ndola o liber&aacute;ndola.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >Los
            marcadores de rebeli&oacute;n no tienen efectos sobre el movimiento (una unidad se puede mover a una
            regi&oacute;n rebelada, apoyar en ella, moverse desde ella, etc).</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >No se
            consigue ingresos de una regi&oacute;n con un marcador de rebeli&oacute;n. Tampoco se pueden reclutar
            unidades en ella.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >La plaga
            y el hambre no afectan a los marcadores de rebeli&oacute;n.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >Una
            regi&oacute;n en rebeli&oacute;n puede ser pacificada mediante una acci&oacute;n de Gastos Extras. Para
            m&aacute;s informaci&oacute;n ver dicha secci&oacute;n.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span>Una regi&oacute;n en
            rebeli&oacute;n puede ser </span><span >sometida</span><span >&nbsp;si una unidad del
            propietario del controlador de la regi&oacute;n (aquel contra el cual la regi&oacute;n se ha revelado) entra
            (a no ser que ya est&eacute; all&iacute;) y permanece con instrucciones de &ldquo;mantener&rdquo; toda una
            Campa&ntilde;a. Al final de la Campa&ntilde;a si la unidad de ej&eacute;rcito o flota permanece
            todav&iacute;a, el marcador de rebeli&oacute;n se retira.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span>Para </span><span
            >liberar</span><span >&nbsp;una regi&oacute;n en rebeli&oacute;n, una unidad, de un
            jugador distinto a aquel contra el que la regi&oacute;n se ha revelado, debe entrar en la regi&oacute;n.
            Cuando esto ocurre, el marcador de rebeli&oacute;n ser retira.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >Cuando se
            realiza un avance por parte de unidades militares de ej&eacute;rcito o flota sobre una regi&oacute;n
            rebelada, y dichas unidades militares son de un jugador distinto a aquel contra el que la regi&oacute;n se
            ha revelado, la regi&oacute;n apoya este avance (es decir, suma &ldquo;uno&rdquo; a la fuerza del bando).
            Esto s&oacute;lo se aplica cuando es s&oacute;lo un jugador el que clama por este apoyo.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span
            >S&iacute; dos o m&aacute;s ordenes de rebeli&oacute;n son dirigidas contra la misma regi&oacute;n
            en la misma Campa&ntilde;a, se obtiene el mismo resultado que si s&oacute;lo se hubiera hecho una orden (es
            decir, no se acumulan marcadores de revoluci&oacute;n).</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >&nbsp;</span></p>
    <h2  id="h.28re2t5wgr7g"><span >&Oacute;rdenes para unidades militares</span></h2>
    <p ><span >&nbsp;</span></p>
    <p ><span >Todas las acciones de cada una de las unidades que hay en juego se llevar&aacute;n a
            cabo simult&aacute;neamente. Hay seis acciones: avanzar, mantener, apoyar, transportar, fusionar o dividir y
            convertirse. No todos los tipos de unidades pueden realizar todas las acciones. Las acciones que pueden
            realizar los distintos tipos de unidades son las siguientes:</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Flota y corsarios: Todas.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Ejercito: Todas menos transportar.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >&nbsp;</span></p>
    <h4  id="h.p3d8gsepcdk4"><span >Avanzar</span></h4>
    <p ><span >&nbsp;</span></p>
    <p ><span >Esta orden indica a la unidad que la recibe que avance a una regi&oacute;n o mar
            adyacente y est&aacute; marcada en el mapa con una flecha continua.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Una flota o un ej&eacute;rcito (excepto cuando es transportado) s&oacute;lo puede
            avanzar a una regi&oacute;n o mar inmediatamente adyacente durante una Campa&ntilde;a. Para ser considerado
            adyacente, la regi&oacute;n o mar tiene que tener un borde com&uacute;n con la localizaci&oacute;n inicial
            de la unidad.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Ya que no puede haber m&aacute;s de dos unidades en la misma regi&oacute;n, una
            unidad no puede avanzar a un lugar ocupado por otra unidad sin provocar un conflicto.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Las unidades de ej&eacute;rcito (excepto cuando son transportadas) s&oacute;lo
            pueden avanzar por tierra, a regiones adyacentes. No pueden avanzar por el mar.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Las unidades de flota y los corsarios pueden avanzar de un mar a otro mar
            adyacente; de un mar a una regi&oacute;n costera que tenga borde directo con ese mar; de una regi&oacute;n
            costera a un mar adyacente y de una regi&oacute;n a la adyacente a lo largo de la l&iacute;nea de costa. No
            pueden avanzar de una regi&oacute;n a otra adyacente a no ser que el movimiento sea a trav&eacute;s de una
            l&iacute;nea de costa.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Una unidad de flota o corsario en una regi&oacute;n costera o en una isla que
            contenga una sola regi&oacute;n (C&oacute;rcega o Cerde&ntilde;a), se considera que est&aacute; a la vez en
            cualquier lugar de su l&iacute;nea de costa. Por ejemplo, una unidad de flota en la regi&oacute;n de
            Calabria puede avanzar al Estrecho de Otranto, al Golfo de Taranto, al Estrecho de Messina o al Mar Tirreno,
            no importa por donde entrara. </span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Esto ser&aacute; as&iacute; siempre que no tenga que pasar por la l&iacute;nea
            costera de otra regi&oacute;n. Por ejemplo, una flota o corsario en Roma no podr&aacute; ir al Mar
            Adri&aacute;tico si entr&oacute; por el Mar Tirreno.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Existen algunas limitaciones al avance por tierra y mar, ver secci&oacute;n REGLAS
            ESPECIALES DEL MAPA.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >&nbsp;</span></p>
    <h4  id="h.cyxqwsoc0eld"><span >Mantener</span></h4>
    <p ><span >&nbsp;</span></p>
    <p ><span >Esta orden indica a la unidad que la recibe que permanezca en el sitio, es decir,
            que no haga nada. Las &oacute;rdenes inv&aacute;lidas o err&oacute;neas se consideran autom&aacute;ticamente
            como ordenes de este tipo. No obstante, tiene una importante utilidad sometiendo rebeliones.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >En el caso en el que el jugador no d&eacute; una orden concreta a una unidad, se
            considerar&aacute; que &eacute;sta tiene la orden de mantener.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >&nbsp;</span></p>
    <h4  id="h.x2ec3tlr5znr"><span >Apoyar</span></h4>
    <p ><span >&nbsp;</span></p>
    <p ><span >Esta orden indica a la unidad que la recibe que apoye a otra unidad en realizar
            sus &oacute;rdenes y en el mapa est&aacute; marcada con una flecha discontinua. Tiene gran importancia en
            los conflictos. El nombre de la regi&oacute;n o mar donde se desea apoyar debe tambi&eacute;n ser indicado y
            en el caso de apoyar a unidades de otro jugador, habr&aacute; que indicar adem&aacute;s a qu&eacute; jugador
            se apoya.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >La unidad que apoya permanece en el sitio, no se mueve al sitio donde se le ha
            indicado que apoye.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Matizaciones:</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >Para
            apoyar, la unidad que apoya tiene que ser capaz de avanzar al sitio o mar donde la acci&oacute;n apoyada
            est&eacute; teniendo lugar. En el caso de apoyar un desplazamiento o un ataque, la unidad que apoya debe ser
            capaz de moverse a la regi&oacute;n a la que la unidad apoyada intenta desplazarse o atacar. </span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >En el
            caso de querer apoyar la fusi&oacute;n de dos unidades, la unidad que apoya lo har&aacute; a la unidad de
            destino en su orden de mantener, de la que la unidad que apoya debe ser adyacente.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >Esta
            capacidad de avance no incluye el transporte. En otras palabras, las unidades militares solo pueden apoyar
            regiones y mares inmediatamente adyacentes. Los ej&eacute;rcitos no pueden apoyar mares, y las flotas no
            pueden apoyar regiones a las que no pueden avanzar normalmente.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >Cualquier
            n&uacute;mero posible de unidades militares puede ser usado para apoyar un mar o regi&oacute;n durante una
            Campa&ntilde;a. Esto aumenta la &ldquo;fuerza&rdquo; de la unidad a la que todas apoyan.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >Para
            apoyar a una unidad que intenta cruzar un paso de monta&ntilde;a o entrar/salir de una regi&oacute;n
            monta&ntilde;osa s&oacute;lo es necesario dar la orden en la segunda campa&ntilde;a que dura el movimiento,
            aunque si lo hace en los dos turnos que dura el movimiento puede protegerla de posibles ataques o conflictos
            con otras unidades.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Transporte</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Esta orden indica a la unidad de flota que la recibe que transporte a la unidad de
            ej&eacute;rcito indicada. Si la unidad pertenece a otro jugador, deber&aacute;s especificarlo. Cuando esta
            orden es realizada, la unidad de ej&eacute;rcito ser&aacute; movida y avanzar&aacute; a la regi&oacute;n
            final.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Matizaciones:</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >Una flota
            o corsario s&oacute;lo puede transportar unidades de ej&eacute;rcito y no m&aacute;s de una por
            Campa&ntilde;a.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >Una
            unidad de flota o corsario debe permanecer en el sitio cuando es usada para transportar.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >Una
            unidad de flota o corsario usados en un transporte no puede tener ninguna otra acci&oacute;n durante esa
            campa&ntilde;a. </span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >Una
            unidad de ej&eacute;rcito transportada debe de acabar su avance en tierra, en una regi&oacute;n.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >Una
            unidad de ej&eacute;rcito transportada puede usar cualquier n&uacute;mero posible de unidades de flota para
            transportarse durante esa campa&ntilde;a. Una unidad de ej&eacute;rcito puede ser transportada a
            trav&eacute;s de cualquier n&uacute;mero de mares y/o regiones costeras que est&eacute;n contiguos en la
            misma campa&ntilde;a si cada uno contiene una unidad de flota con la orden de transportar a ese
            ej&eacute;rcito. La unidad de ej&eacute;rcito ha de tener &oacute;rdenes de avanzar a la regi&oacute;n
            deseada (si no las &oacute;rdenes de transportar no utilizadas se toman como acciones inv&aacute;lidas). Una
            unidad transportada s&oacute;lo puede tener &oacute;rdenes de avanzar, nunca se le podr&aacute;n dar
            &oacute;rdenes de apoyo.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >Una
            unidad de ej&eacute;rcito s&oacute;lo puede ser transportada a regiones a las cuales la unidad de flota
            misma podr&iacute;a moverse legalmente.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >SUGERENCIA Y ESTRATEGIA: la orden de transportar es una de las herramientas
            m&aacute;s valiosas con las que cuenta el jugador para aumentar la movilidad de sus tropas, y una de las
            &oacute;rdenes que hacen m&aacute;s valiosas las flotas. Por eso es buena idea tener las flotas bien
            esparcidas por los mares, estableciendo l&iacute;neas de transporte. </span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >&nbsp;</span></p>
    <h4  id="h.xkppyrtgfypz"><span >Fusionar o dividir</span></h4>
    <p ><span >&nbsp;</span></p>
    <p ><span >Es posible fusionar dos o m&aacute;s unidades propias del mismo tipo que se
            encuentren en regiones o mares adyacentes. La unidad que tiene la orden de fusionar abandonar&aacute; su
            posici&oacute;n, desapareciendo del mapa, para sumar su fuerza a la de la unidad de destino, que siempre
            debe tener la orden de mantener.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >No se pueden fusionar unidades de distinto tipo, ni de distinto jugador.</span>
    </p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Por otra parte, una unidad de fuerza 2 o superior puede tener la orden de dividir
            su fuerza. En este caso la unidad se dividir&aacute; en 2 o m&aacute;s unidades, quedando una en la
            regi&oacute;n de origen y el resto teniendo que abandonarla a una regi&oacute;n o mar adyacente. El jugador
            elegir&aacute; de qu&eacute; manera se divide la fuerza de la unidad.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >En el caso en que la unidad que abandone una regi&oacute;n como resultado de una
            divisi&oacute;n cuenta a efectos pr&aacute;cticos como un avance. Si no pudiera desplazarse por culpa de un
            conflicto con otras unidades, la divisi&oacute;n no se llevar&aacute; a cabo.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >&nbsp;</span></p>
    <h4  id="h.oz1h0tet2o5y"><span >Conversi&oacute;n</span></h4>
    <p ><span >&nbsp;</span></p>
    <p ><span >La unidad que recibe esta orden se transforma en otro tipo de unidad. Con esta
            orden, tambi&eacute;n se puede ordenar a una unidad que desaparezca del mapa, tal y como si se hubiera
            dejado de mantener en la Campa&ntilde;a de Primavera. Retirar una unidad puede no tener mucho sentido
            aparente pero puede ser &uacute;til en el contexto de una negociaci&oacute;n diplom&aacute;tica en la que se
            exija a un jugador que elimine ciertas unidades.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Matizaciones:</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >Un
            ej&eacute;rcito s&oacute;lo puede ser convertido en flota si se halla en una regi&oacute;n con
            puerto.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >Una flota
            o un ej&eacute;rcito no pueden ser convertidos en corsarios ni viceversa.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >Una
            unidad militar de un poder mayor no puede tener &oacute;rdenes de ser convertida en una unidad militar de
            otro poder mayor.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >Una
            unidad convertida en otro tipo de unidad mantendr&aacute; su fuerza.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >&nbsp;</span></p>
    <h1  id="h.j8zuqctgiq6t"><span >REGLAS ESPECIALES DEL MAPA</span></h1>
    <p ><span >&nbsp;</span></p>
    <p ><span >En esta secci&oacute;n vamos a ver una serie de reglas especiales que se originan en
            torno a ciertos puntos estrat&eacute;gicos del mapa. Son las siguientes.</span></p>
    <p ><span >&nbsp;</span></p>
    <h2  id="h.475k10e1q09c"><span >Gibraltar</span></h2>
    <p ><span >&nbsp;</span></p>
    <p ><span >La regi&oacute;n de Gibraltar separa el estrecho de Gibraltar del Mar de
            Albor&aacute;n. Cualquier unidad de flota que se encuentre dentro de la regi&oacute;n bloquear&aacute; el
            paso entre esos dos mates. Una unidad de ej&eacute;rcito no bloquear&aacute; el paso.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >&nbsp;</span></p>
    <h2  id="h.rcxbh64bktsx"><span >Constantinopla</span></h2>
    <p ><span >&nbsp;</span></p>
    <p ><span >La regi&oacute;n de Constantinopla separa el Mar Egeo del Delta del Danubio.
            Cualquier unidad de flota que se encuentre dentro de la regi&oacute;n bloquear&aacute; el paso entre esos
            dos mares. Una unidad de ej&eacute;rcito no bloquear&aacute; el paso.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Existe la posibilidad de que un jugador controle uno o los dos estrechos. El jugador
            que controle un estrecho, deber&aacute; indicar a quien deja pasar y a quien no, y bajo qu&eacute;
            condiciones.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >SUGERENCIA Y ESTRATEGIA: el control de los estrechos es importante. Permite poner un
            obst&aacute;culo a los oponentes en su avance por el territorio. Una opci&oacute;n interesante es la de no
            dejar pasar a nadie excepto a los aliados, aunque siempre se puede recurrir al peaje para recaudar
            fondos.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >&nbsp;</span></p>
    <h2  id="h.u62u3rewuh2s"><span >Monta&ntilde;as</span></h2>
    <p ><span >&nbsp;</span></p>
    <p ><span >Algunas de las regiones del mapa contienen monta&ntilde;as. Estas monta&ntilde;as son
            obst&aacute;culos que limitan el movimiento por tierra. Existen dos tipos:</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >Regiones
            monta&ntilde;osas</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Son regiones compuestas fundamentalmente por monta&ntilde;as y cordilleras.
            Entrar y salir de ellas por tierra implica una penalizaci&oacute;n al movimiento. Una unidad con
            &oacute;rdenes de entrar o salir de una regi&oacute;n monta&ntilde;osa al siguiente movimiento se
            quedar&aacute; en pausa y salr&aacute; en el mapa con el icono (zzz).</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >Pasos de
            monta&ntilde;a</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Algunas regiones en el mapa tienen una cordillera que las separa y limita el
            movimiento entre ellas. Para cruzar de una regi&oacute;n a otra separada de la primera por un paso de
            monta&ntilde;a una unidad tardar&aacute; dos campa&ntilde;as en vez de una, como ser&iacute;a normal. Esta
            penalizaci&oacute;n no afectar&aacute; a las dem&aacute;s regiones adyacentes entre las cuales no haya un
            paso de monta&ntilde;a.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Ejemplo: una unidad en Catalu&ntilde;a tardar&aacute; 2 campa&ntilde;as en cruzar a
            Languedoc, pero s&oacute;lo 1 para desplazarse a Arag&oacute;n o Valencia.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Una unidad que cruce un paso de monta&ntilde;a o avance hacia una regi&oacute;n
            monta&ntilde;osa se considera que est&aacute; en la regi&oacute;n de origen hasta que el movimiento se haga
            efectivo. Si por alg&uacute;n motivo el movimiento se interrumpe en alg&uacute;n momento, la unidad que
            intenta desplazarse se quedar&aacute; en el sitio o se retirar&aacute; seg&uacute;n sea el caso.</span></p>
    <p ><span >&nbsp;</span></p>
    <h2  id="h.6xpaavy9hblk"><span >Cofres</span></h2>
    <p ><span >&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;En cada fase de primavera aparecer&aacute;n 3 cofres
            de manera aleatoria en cualquier territorio de mar. La flota (no corsario) que permanezca en este territorio
            en la fase de Oto&ntilde;o, recibir&aacute; una recompensa entre 1 y 6 ducados, seg&uacute;n una tirada de
            dados. Si al finalizar la fase de Oto&ntilde;o no hay flota sobre el territorio donde est&aacute; el cofre,
            este desaparecer&aacute;.</span></p>
