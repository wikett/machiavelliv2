    <h1  id="h.7yzlrgpyyq93"><span >EL MAPA Y SUS ELEMENTOS</span></h1>
    <p ><span >&nbsp;</span></p>
    <p ><span >Para que cada jugador permanezca informado de la situaci&oacute;n de la partida, en
            cada turno se publicar&aacute; un mapa en el que se reflejan las acciones de los movimientos del
            &uacute;ltimo turno. </span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Podemos dividir los elementos del mapa en dos: constantes (regiones, mares, puertos,
            monta&ntilde;as, etc) y m&oacute;viles (unidades ej&eacute;rcito, flotas, marcadores de hambre, etc).</span>
    </p>
    <p ><span >&nbsp;</span></p>
    <p ><span >&nbsp;</span></p>
    <h2  id="h.tvdj1tyndto1"><span >Elementos constantes</span></h2>
    <p ><span >&nbsp;</span></p>
    <p ><span >Regiones</span></p>
    <p ><span >Las zonas de tierra est&aacute;n divididas en regiones. Las regiones son
            territorios aut&oacute;nomos en los que las unidades de los jugadores pueden estar. Las regiones limitan a
            su vez con cierto n&uacute;mero de regiones o mares adyacentes. </span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Las regiones est&aacute;n normalmente controladas por un jugador. El color de la
            regi&oacute;n es el del jugador que la est&eacute; controlando actualmente (el color cambiar&aacute;, a
            medida que cambie el controlador a lo largo del juego). Algunas regiones tienen color blanco, lo que indica
            que no est&aacute;n controladas por nadie y pueden ser tomadas sin entrar necesariamente en conflicto
            directo con otro jugador.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Mares</span></p>
    <p ><span >Los mares siempre tienen nombres en min&uacute;scula y son similares a las
            regiones, aunque mucho m&aacute;s grandes y solamente las flotas pueden moverse por ellos. Adem&aacute;s,
            los mares no tienen nunca controlador y su color siempre es azul claro.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Puertos</span></p>
    <p ><span >Representados por un ancla, se encuentran solo en algunas regiones costeras. Las
            flotas s&oacute;lo pueden ser creadas en regiones en las que hay puerto. Aun as&iacute;, un barco puede
            avanzar a toda regi&oacute;n costera normalmente aunque no haya puerto en esa regi&oacute;n.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Oro</span></p>
    <p ><span >Representado por dos monedas, bajo el nombre de la provincia, indica la cantidad
            de oro que esta provincia aporta a la econom&iacute;a del jugador que la posea.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Poblaci&oacute;n</span></p>
    <p ><span >Representado por uno o varios hombrecitos, representa la cantidad de
            poblaci&oacute;n que tiene la provincia y que aporta al c&oacute;mputo total de poblaci&oacute;n de cada una
            de las potencias. Una provincia no puede producir ej&eacute;rcitos ni flotas de fuerza superior a su
            poblaci&oacute;n.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Monta&ntilde;as</span></p>
    <p ><span >En el mapa las monta&ntilde;as aparecen de dos maneras, las regiones
            monta&ntilde;osas y los pasos de monta&ntilde;a. En los dos casos son elementos fijos del mapa que suponen
            una penalizaci&oacute;n en el movimiento de las unidades de tierra.</span></p>
    <p ><span >&nbsp;</span></p>
    <h2  id="h.oos4u3dzk79r"><span >Elementos m&oacute;viles</span></h2>
    <p ><span >&nbsp;</span></p>
    <p ><span >Aunque por el nombre lo puedan parecer, no todos ellos se &ldquo;mueven&rdquo; por
            el tablero. Lo que s&iacute; tienen en com&uacute;n es que pueden dejar de aparecer en el mapa en un
            determinado momento, a diferencia de los elementos fijos.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&nbsp; &nbsp;</span><span >&nbsp; &nbsp; Unidades de ej&eacute;rcito &nbsp;
            &nbsp;</span></p>
    <p ><span >Representadas por un granadero. Cada ej&eacute;rcito tiene la bandera de la
            potencia que lo posee y un n&uacute;mero que indica su nivel o fuerza. Se encuentran siempre en las
            regiones, nunca en mares.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Unidades de flota</span></p>
    <p ><span >Representadas por un barco. Al igual que los ej&eacute;rcitos, vienen
            acompa&ntilde;adas de su bandera y su fuerza. Se pueden mover por los mares o regiones costeras, nunca
            provincias que no tengan contacto con el mar. </span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Corsarios</span></p>
    <p ><span >Representadas por una flota sin bandera. A efectos pr&aacute;cticos es igual que
            una unidad de flota, con la salvedad de que la identidad del propietario es secreta y que cuando mueven a
            una regi&oacute;n costera no la conquistan. Los corsarios s&oacute;lo bloquean la provincia y evitan que el
            jugador propietario de la provincia pueda reclutar unidades nuevas y pueda recaudar ducados en ella, que
            ir&aacute;n directamente al propietario de la unidad de corsarios. </span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Unidades aut&oacute;nomas</span></p>
    <p ><span >Son ej&eacute;rcitos que defienden las naciones neutrales. Nunca atacan ni se
            mueven y s&oacute;lo suponen un obst&aacute;culo al avance de los jugadores. En caso de ser derrotados en un
            conflicto con un jugador, estos ser&aacute;n retirados del tablero.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Marcador de hambre</span></p>
    <p ><span >Representado por una H. Indica que la regi&oacute;n donde est&aacute; padece
            hambre. Ver &ldquo;Desastre naturales&rdquo; p&aacute;g. 16.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Marcador de revoluci&oacute;n</span></p>
    <p ><span >Representado por una R recuadrada, con el color de alguno de los pa&iacute;ses de
            los jugadores. Indica que la regi&oacute;n donde est&aacute; en revuelta contra dicho color. Ver
            &ldquo;Rebeli&oacute;n&rdquo;, p&aacute;g. &iexcl;Error! Marcador no definido..</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >&nbsp;</span></p>
    <h2  id="h.qe0qkve2uf6r"><span >Pa&iacute;s natal</span></h2>
    <p ><span >&nbsp;</span></p>
    <p ><span >El pa&iacute;s natal de un jugador es el conjunto de regiones que empiezan el juego
            siendo controladas por dicho jugador, salvo algunas excepciones que ser&aacute;n indicadas al principio de
            la partida. Para ganar, una de las condiciones es absorber uno o dos de los pa&iacute;ses natales de otros
            jugadores.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >A diferencia del controlador, que puede variar a menudo a lo largo del juego, el
            pa&iacute;s natal de un jugador es el mismo hasta que el jugador es eliminado. La &uacute;nica manera que
            hay de que las regiones del &ldquo;pa&iacute;s natal&rdquo; de un jugador pasen a formar parte del
            &ldquo;pa&iacute;s natal&rdquo; de otro es cuando ese jugador controle todas las regiones del
            &ldquo;pa&iacute;s natal&rdquo; del otro al final de una Campa&ntilde;a. En ese caso, las regiones son
            absorbidas y se tratan exactamente igual que s&iacute; el jugador hubiera empezado el juego con
            ellas.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Si un jugador no mantiene el control de al menos una de las regiones de su
            pa&iacute;s natal, ese jugador es eliminado del juego y todas sus unidades militares disueltas.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Muchas de las regiones del mapa no forman parte del pa&iacute;s natal de
            nadie.</span></p>
    <p ><span >&nbsp;</span></p>
