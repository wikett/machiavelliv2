    <h1  id="h.9uvp7okgaovj"><span ></span></h1>
    <h1  id="h.ds5n11e3iews"><span >FASE DE AJUSTES MILITARES</span></h1>
    <p ><span >&nbsp;</span></p>
    <p ><span >En esta fase, realizada por el jugador al inicio de cada Campa&ntilde;a de Primavera,
            &eacute;ste deber&aacute; indicar cuantas unidades militares crea y d&oacute;nde. Tambi&eacute;n
            deber&aacute; indicar cu&aacute;les de sus unidades ya existentes mantiene y cu&aacute;les disuelve. El
            coste de mantenimiento para cada unidad es igual al coste de reclutamiento o contrataci&oacute;n. Las
            unidades no mantenidas se retiran del juego.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >El jugador indicar&aacute; el tipo de unidad que recluta, en qu&eacute; regi&oacute;n
            y su fuerza. Puede reclutar y mantener todas las tropas nuevas que desee, ajust&aacute;ndose siempre a las
            siguientes limitaciones:</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >El
            jugador debe tener suficientes ducados en sus arcas para asumir el coste de contrataci&oacute;n y
            mantenimiento de todas sus unidades.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >La fuerza
            de la unidad reclutada no puede exceder al marcador de poblaci&oacute;n de la regi&oacute;n en la que ha
            sido reclutada.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >El
            c&oacute;mputo de la fuerza de todas las unidades de un jugador no puede exceder a la poblaci&oacute;n total
            de su imperio.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >La
            regi&oacute;n en la que se crea la unidad tiene que pertenecer al pa&iacute;s natal de jugador, estar
            controlada por &eacute;ste y no estar ocupada ya por otra unidad.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >No se
            puede reclutar ning&uacute;n tipo de unidad en una regi&oacute;n en la que ya haya otra unidad, del tipo que
            sea.</span></p>
    <p ><span >&nbsp;</span></p>
