    <h2  id="h.i2wmje8k29cm"><span >Corsarios</span></h2>
    <p ><span >&nbsp;</span></p>
    <p ><span >Cada nivel de fuerza de una unidad de corsarios tiene un coste de 6 ducados y pueden
            ser contratadas en cualquier mar del mapa. Los corsarios no tienen una bandera asignada y la identidad del
            jugador que los ha contratado es secreta para los dem&aacute;s jugadores.</span></p>
