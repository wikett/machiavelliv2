    <h2  id="h.sg2fgd1furv4"><span >C&oacute;mo determinar el control</span></h2>
    <p ><span >&nbsp;</span></p>
    <p ><span >En primer lugar, hay que puntualizar que las masas de agua no tienen controlador
            propiamente dicho, aunque podr&iacute;a decirse que la flota que est&aacute; en ellas es el
            &ldquo;controlador&rdquo;. El controlador de una regi&oacute;n (indicado por su color) es el jugador que
            controle a la &uacute;ltima unidad de ej&eacute;rcito o flota que haya estado all&iacute;. Dicha unidad
            puede encontrarse all&iacute; todav&iacute;a o estar en otro lugar, dejando la regi&oacute;n
            vac&iacute;a.</span></p>
