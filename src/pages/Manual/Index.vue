    <h1  id="h.goo287n53bdt"><span >ECONOM&Iacute;A Y POBLACI&Oacute;N</span></h1>
    <p ><span >&nbsp;</span></p>
    <p ><span >En esta versi&oacute;n de Machiavelli la econom&iacute;a y la poblaci&oacute;n son
            conceptos fundamentales a la hora de gestionar tus fuerzas.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Cada regi&oacute;n tiene en el mapa bajo su nombre los marcadores de oro y
            poblaci&oacute;n. &Eacute;stos vienen representados por uno o varios aldeanos y unas monedas, ducados. La
            cantidad de poblaci&oacute;n y oro que cada regi&oacute;n aporta al imperio se suman a las dem&aacute;s
            determinando los ingresos de cada jugador y la poblaci&oacute;n total.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >&nbsp;</span></p>
    <h2  id="h.tute5plhjeia"><span >Poblaci&oacute;n</span></h2>
    <p ><span >&nbsp;</span></p>
    <p ><span >La poblaci&oacute;n es un factor que est&aacute; directamente relacionado con la
            capacidad que tiene un imperio para reclutar ej&eacute;rcitos. Cada regi&oacute;n tiene un valor de
            poblaci&oacute;n que indica la fuerza m&aacute;xima de las unidades que se pueden reclutar en dicha
            regi&oacute;n. Es decir, si una regi&oacute;n tiene un valor de poblaci&oacute;n 2, en ella no se
            podr&aacute;n reclutar unidades militares, ya sean ej&eacute;rcitos o flotas, de fuerza mayor a 2.</span>
    </p>
    <p ><span >&nbsp;</span></p>
    <p ><span >La poblaci&oacute;n de un imperio es la suma de los indicadores de poblaci&oacute;n
            de cada una de las regiones que componen ese imperio. Esto limita la potencia militar de dicho imperio, no
            pudiendo ser mayor la suma de las fuerzas de todas las unidades militares a la poblaci&oacute;n total del
            imperio. Si se diera el caso, al llegar la fase de ajustes militares, el jugador deber&aacute; disolver
            tantas unidades como sea necesario para que el primer valor est&eacute; igual o por debajo del
            segundo.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >&nbsp;</span></p>
    <h2  id="h.8popsnx1lqce"><span >Ducados</span></h2>
    <p ><span >&nbsp;</span></p>
    <p ><span >Cada regi&oacute;n aporta una cantidad determinada de ducados al imperio, tantos como
            indique su marcador en el mapa. Cada jugador recaudar&aacute; los ducados de todas sus regiones en la fase
            de ingresos, adem&aacute;s de una renta variable. Una regi&oacute;n con un marcador de Hambre o
            Revoluci&oacute;n no produce ingresos. Tambi&eacute;n recibir&aacute; un ducado por cada regi&oacute;n de
            mar que controle.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >La renta variable depende de tiradas de dado, y suma entre 1 y 6 a las arcas del
            tesoro de cada jugador.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Todo el oro ganado por el jugador en la fase de ajustes militares pasa a sus Arcas
            del Tesoro. La informaci&oacute;n de la cantidad de oro en las arcas es privada. Es decir, un jugador
            s&oacute;lo sabe con certeza cu&aacute;nto dinero tiene &eacute;l (aunque puede intuir cu&aacute;nto dinero
            tienen otros jugadores observando sus ingresos y gastos a lo largo de la partida).</span></p>
