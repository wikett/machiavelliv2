    <h1  id="h.ljtnd8o4irc6"><span >ASESINATOS</span></h1>
    <p ><span >&nbsp;</span></p>
    <p ><span >Existe una doceava orden de Gastos Extra, que es la de &ldquo;asesinar&rdquo; a otro
            jugador. No ha sido descrita en la secci&oacute;n de &ldquo;Gastos Extra&rdquo; ya que los asesinatos tienen
            un funcionamiento m&aacute;s complejo que el del resto de las acciones de Gastos Extra.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Los intentos de asesinato se resuelven en su propia fase, antes incluso que la de
            Gastos Extras, y la cantidad de ducados a emplear debe ser m&uacute;ltiplo de 12, con un m&aacute;ximo de
            36. Con cada 12d gastados se compra un n&uacute;mero en un dado de seis caras. Si sale uno de los
            n&uacute;meros comprados el intento de asesinato tiene &eacute;xito. Si no sale uno de los n&uacute;meros,
            el intento fracasa y el dinero se pierde. </span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Se puede realizar m&aacute;s de un intento de asesinato sobre un mismo jugador en el
            mismo turno, pero cada jugador no puede realizar m&aacute;s de uno. Adem&aacute;s, dos o m&aacute;s
            jugadores no pueden ponerse de acuerdo para comprar conjuntamente los n&uacute;meros del dado, los intentos
            se hacen uno por uno. De todos modos, dos asesinatos con &eacute;xito en la misma Campa&ntilde;a contra el
            mismo jugador tienen el mismo efecto que uno s&oacute;lo.</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span >Un jugador asesinado no queda ni mucho menos fuera del juego. El asesinato representa
            la muerte del actual l&iacute;der del poder mayor, con la llegada de un periodo de caos que puede ser muy
            perjudicial. A efectos de juego:</span></p>
    <p ><span >&nbsp;</span></p>
    <p ><span>&bull;</span><span >&nbsp; &nbsp; &nbsp; &nbsp; </span><span >Las
            acciones de todas las unidades del jugador asesinado en ese turno pasan autom&aacute;ticamente a mantener.
